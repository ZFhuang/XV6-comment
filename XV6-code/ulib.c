//返回字符串中选定字符出现处的指针
char*
strchr(const char *s, char c)
{
  for(; *s; s++)
    if(*s == c)
      return (char*)s;
  return 0;
}

//读取buf中换行符前的字符串
char*
gets(char *buf, int max)
{
  int i, cc;
  char c;

  //循环直至最大值
  for(i=0; i+1 < max; ){
	  //读取
    cc = read(0, &c, 1);
    if(cc < 1)
      break;
    buf[i++] = c;
	//找到换行符跳除
    if(c == '\n' || c == '\r')
      break;
  }
  //将其后一个字符改为结束符得到新串
  buf[i] = '\0';
  return buf;
}

int
stat(const char *n, struct stat *st)
{
  int fd;
  int r;

  fd = open(n, O_RDONLY);
  if(fd < 0)
    return -1;
  r = fstat(fd, st);
  close(fd);
  return r;
}

int
atoi(const char *s)
{
  int n;

  n = 0;
  while('0' <= *s && *s <= '9')
    n = n*10 + *s++ - '0';
  return n;
}

void*
memmove(void *vdst, const void *vsrc, int n)
{
  char *dst;
  const char *src;

  dst = vdst;
  src = vsrc;
  while(n-- > 0)
    *dst++ = *src++;
  return vdst;
}
